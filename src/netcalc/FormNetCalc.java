    private void btnNetAddressActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNetAddressActionPerformed
        String s = JOptionPane.showInputDialog("Digite o endereço de rede", nc.getNetAddress());
        if (s != null) {
            try {
                nc.setNetAddress(s);
            }
            catch (IllegalArgumentException e) {
                JOptionPane.showMessageDialog(this, "Endereço de rede inválido", "Atenção!", JOptionPane.ERROR_MESSAGE);
            }
            finally {
                setNetAddress();
                setSubNetsList();
            }
        }
}//GEN-LAST:event_btnNetAddressActionPerformed

