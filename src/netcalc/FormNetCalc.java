    private void initNet() {
        busy = true;

        setNetAddress();

        DecimalFormat df = new DecimalFormat(decimalFormat);

        for (int i = 0; i <= nc.getBits(); i++) {
            cmbNetBits.addItem(String.valueOf(i));
            cmbHostBits.addItem(String.valueOf(i));
            cmbNets.addItem(df.format(Math.pow(2, i)));
            cmbHosts.addItem(df.format(Math.pow(2, i)));
        }
        busy = false;

        setNetBits();
    }

    private void initSubNet() {
        busy = true;

        cmbSubNetBits.removeAllItems();
        cmbSubHostBits.removeAllItems();
        cmbSubNets.removeAllItems();
        cmbSubHosts.removeAllItems();

        DecimalFormat df = new DecimalFormat();

        for (int i = 0; i <= nc.getHostBits(); i++) {
            cmbSubNetBits.addItem(String.valueOf(i));
            cmbSubHostBits.addItem(String.valueOf(i));
            cmbSubNets.addItem(df.format(Math.pow(2, i)));
            cmbSubHosts.addItem(df.format(Math.pow(2, i)));
        }
        busy = false;

        cmbSubNetBitsActionPerformed(null);
    }

    private long getComboInt(JComboBox c) {
        DecimalFormat df = new DecimalFormat(decimalFormat);

        Number n = null;
        try {
            n = df.parse(String.valueOf(c.getSelectedItem()));
        } catch (ParseException e) {
        }
        
        return n.longValue();
    }
    private long getComboIntAt(JComboBox c, int i) {
        DecimalFormat df = new DecimalFormat(decimalFormat);

        Number n = null;
        try {
            n = df.parse(String.valueOf(c.getItemAt(i)));
        } catch (ParseException e) {
        }

        return n.longValue();
    }
    private void setComboInt(JComboBox c, long value) {
        for(int i = 0; i < c.getItemCount(); i++) {
            if (value == getComboIntAt(c, i)) {
                c.setSelectedIndex(i);
                break;
            }
        }
    }

